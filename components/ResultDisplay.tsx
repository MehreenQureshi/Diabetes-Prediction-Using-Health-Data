
import React from 'react';
import { HealthData, RiskLevel } from '../types';
import RiskIndicator from './RiskIndicator';
import DataChart from './DataChart';

interface ResultDisplayProps {
  prediction: { risk: RiskLevel; explanation: string } | null;
  isLoading: boolean;
  error: string | null;
  userData: HealthData;
}

const LoadingSpinner: React.FC = () => (
    <div className="flex flex-col items-center justify-center space-y-4">
        <svg className="animate-spin h-10 w-10 text-sky-500" xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24">
            <circle className="opacity-25" cx="12" cy="12" r="10" stroke="currentColor" strokeWidth="4"></circle>
            <path className="opacity-75" fill="currentColor" d="M4 12a8 8 0 018-8V0C5.373 0 0 5.373 0 12h4zm2 5.291A7.962 7.962 0 014 12H0c0 3.042 1.135 5.824 3 7.938l3-2.647z"></path>
        </svg>
        <p className="text-slate-500 dark:text-slate-400">Generating AI-powered analysis...</p>
    </div>
);

const ResultDisplay: React.FC<ResultDisplayProps> = ({ prediction, isLoading, error, userData }) => {
  if (isLoading) {
    return <LoadingSpinner />;
  }
  
  if (error) {
    return <div className="text-center text-red-500">{error}</div>;
  }
  
  if (!prediction) {
    return (
        <div className="text-center">
            <h3 className="text-lg font-medium text-slate-700 dark:text-slate-300">Your Results Will Appear Here</h3>
            <p className="mt-2 text-sm text-slate-500 dark:text-slate-400">Fill in your health data and click "Predict Risk" to see your personalized analysis.</p>
        </div>
    );
  }

  const handleDownloadReport = () => {
    if (!prediction) return;

    const escapeLatex = (text: string) => {
        let escaped = text
            .replace(/\\/g, '\\textbackslash{}')
            .replace(/&/g, '\\&')
            .replace(/%/g, '\\%')
            .replace(/\$/g, '\\$')
            .replace(/#/g, '\\#')
            .replace(/_/g, '\\_')
            .replace(/{/g, '\\{')
            .replace(/}/g, '\\}')
            .replace(/~/g, '\\textasciitilde{}')
            .replace(/\^/g, '\\textasciicircum{}');
        
        // Convert simple markdown to LaTeX
        escaped = escaped.replace(/\*\*(.*?)\*\*/g, '\\textbf{$1}');
        escaped = escaped.replace(/(\r\n|\n|\r)/gm, "\n\n"); // Normalize newlines to paragraphs
        
        return escaped;
    };

    const date = new Date().toLocaleDateString('en-CA'); // YYYY-MM-DD
    const explanationForLatex = escapeLatex(prediction.explanation);

    const latexContent = `
\\documentclass[11pt, a4paper]{article}
\\usepackage[utf8]{inputenc}
\\usepackage{geometry}
\\geometry{a4paper, margin=1in}
\\usepackage{graphicx}
\\usepackage{hyperref}
\\usepackage{amsmath}
\\usepackage{amsfonts}
\\usepackage{amssymb}
\\usepackage{enumitem}

\\title{Diabetes Risk Assessment Report}
\\author{Generated by the Diabetes Risk Predictor Application}
\\date{${date}}

\\begin{document}
\\maketitle

\\section*{Patient Health Metrics}
The following data was provided for this assessment:
\\begin{itemize}[leftmargin=*]
    \\item \\textbf{Pregnancies:} ${userData.Pregnancies}
    \\item \\textbf{Glucose:} ${userData.Glucose} mg/dL
    \\item \\textbf{Blood Pressure:} ${userData.BloodPressure} mm Hg
    \\item \\textbf{Skin Thickness:} ${userData.SkinThickness} mm
    \\item \\textbf{Insulin:} ${userData.Insulin} mu U/ml
    \\item \\textbf{BMI (Body Mass Index):} ${userData.BMI}
    \\item \\textbf{Diabetes Pedigree Function:} ${userData.DiabetesPedigreeFunction}
    \\item \\textbf{Age:} ${userData.Age} years
\\end{itemize}

\\section*{Assessment Result}
Based on the provided data, the estimated risk level for diabetes is:
\\begin{center}
    \\huge{\\textbf{${prediction.risk}}}
\\end{center}

\\section*{AI-Powered Analysis and Recommendations}
${explanationForLatex}

\\vfill

\\hrule
\\vspace{0.2cm}
\\begin{center}
    \\small
    \\textit{Disclaimer: This report is for informational purposes only and is not a substitute for professional medical advice, diagnosis, or treatment. Always seek the advice of your physician or other qualified health provider with any questions you may have regarding a medical condition.}
\\end{center}

\\end{document}
    `.trim();

    const blob = new Blob([latexContent], { type: 'text/x-latex' });
    const url = URL.createObjectURL(blob);
    const a = document.createElement('a');
    a.href = url;
    a.download = 'diabetes_risk_report.tex';
    document.body.appendChild(a);
    a.click();
    document.body.removeChild(a);
    URL.revokeObjectURL(url);
  };


  // Simple markdown to HTML
  const formattedExplanation = prediction.explanation
    .replace(/\*\*(.*?)\*\*/g, '<strong>$1</strong>')
    .replace(/\n\s*-\s(.*?)/g, '<li class="ml-4 list-disc">$1</li>')
    .replace(/<\/li><li/g, '</li><li') // Clean up spacing between list items
    .replace(/\n/g, '<br />');


  return (
    <div className="w-full space-y-6 animate-fade-in">
        <div>
            <h3 className="text-lg font-semibold text-center mb-2">Your Estimated Risk Level</h3>
            <RiskIndicator risk={prediction.risk} />
        </div>
        <div className="w-full h-64">
            <DataChart userData={userData} />
        </div>
        <div className="text-sm text-slate-600 dark:text-slate-300 prose prose-sm dark:prose-invert max-w-none prose-strong:text-slate-700 dark:prose-strong:text-slate-200" dangerouslySetInnerHTML={{ __html: formattedExplanation }} />

        <div className="pt-4 text-center">
            <button
                onClick={handleDownloadReport}
                className="inline-flex items-center px-4 py-2 border border-slate-300 dark:border-slate-600 text-sm font-medium rounded-md shadow-sm text-slate-700 dark:text-slate-200 bg-white dark:bg-slate-700 hover:bg-slate-50 dark:hover:bg-slate-600 focus:outline-none focus:ring-2 focus:ring-offset-2 focus:ring-sky-500 transition-colors"
            >
                <svg xmlns="http://www.w3.org/2000/svg" className="h-5 w-5 mr-2" fill="none" viewBox="0 0 24 24" stroke="currentColor">
                    <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M4 16v1a3 3 0 003 3h10a3 3 0 003-3v-1m-4-4l-4 4m0 0l-4-4m4 4V4" />
                </svg>
                Download LaTeX Report (.tex)
            </button>
        </div>
    </div>
  );
};

export default ResultDisplay;
